package com.cafe24.iumium.commoncode.dto;

public class CountryCode {
	private String countryCode;
	private String recordId;
	private String countryName;
	private String countryIsUsed;
	private String countryIsChanged;
	private String countryReasonForChange;
	private String countryChangedDate;
	private String countryRegisteredDate;
	
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getCountryIsUsed() {
		return countryIsUsed;
	}
	public void setCountryIsUsed(String countryIsUsed) {
		this.countryIsUsed = countryIsUsed;
	}
	public String getCountryIsChanged() {
		return countryIsChanged;
	}
	public void setCountryIsChanged(String countryIsChanged) {
		this.countryIsChanged = countryIsChanged;
	}
	public String getCountryReasonForChange() {
		return countryReasonForChange;
	}
	public void setCountryReasonForChange(String countryReasonForChange) {
		this.countryReasonForChange = countryReasonForChange;
	}
	public String getCountryChangedDate() {
		return countryChangedDate;
	}
	public void setCountryChangedDate(String countryChangedDate) {
		this.countryChangedDate = countryChangedDate;
	}
	public String getCountryRegisteredDate() {
		return countryRegisteredDate;
	}
	public void setCountryRegisteredDate(String countryRegisteredDate) {
		this.countryRegisteredDate = countryRegisteredDate;
	}
	
	
}
